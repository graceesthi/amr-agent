"""Generate the SYNTHETIC seed dataset: data/surveillance.json + data/seed/*.md.

    python scripts/build_dataset.py

⚠️  READ THIS ⚠️
The figures produced here are SYNTHETIC. They are internally consistent and
epidemiologically plausible in shape, but they are NOT real surveillance data
and must never be cited as such. They exist so that `python src/agent.py` works
from a clean clone with no network access and no manual data step — which is
what the repository-quality gate requires.

For real numbers, replace the corpus with WHO GLASS and ECDC EARS-Net reports;
data/README.md gives the procedure. The retrieval pipeline is source-agnostic —
drop .md/.txt files into data/seed/ and they are indexed on the next run.

Design of the generator
-----------------------
Narrative documents and the structured table are emitted from ONE source of
truth (SERIES below), so `search_amr_literature` and `get_resistance_profile`
never disagree. That matters: a corpus that contradicts its own structured
table would make the critic's grounding check fire on every run, and we would
be measuring a data bug rather than the agent.
"""

