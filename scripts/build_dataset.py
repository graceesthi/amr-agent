def main() -> None:
    records = build_records()
    JSON_PATH.parent.mkdir(parents=True, exist_ok=True)
    JSON_PATH.write_text(
        json.dumps(
            {
                "_warning": "SYNTHETIC DATA generated by scripts/build_dataset.py. "
                "Not real surveillance. Do not cite.",
                "generated_by": "scripts/build_dataset.py",
                "n_records": len(records),
                "records": records,
            },
            ensure_ascii=False,
            indent=2,
        ),
        encoding="utf-8",
    )
    write_corpus(records)
    write_adversarial()

    print(f"Wrote {len(records)} records → {JSON_PATH.relative_to(ROOT)}")
    print(f"Wrote {len(list(SEED_DIR.glob('*.md')))} documents → data/seed/")
    print("Wrote 1 adversarial fixture → data/adversarial/")


if __name__ == "__main__":
    main()
